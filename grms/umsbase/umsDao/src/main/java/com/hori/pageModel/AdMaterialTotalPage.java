package com.hori.pageModel;

import java.io.Serializable;

import com.hori.dao.queryBean.BaseQueryBean;

public class AdMaterialTotalPage extends BaseQueryBean implements Serializable {

	private String id;
	/**
	 * 开始的投放日期
	 */
	private String startTime;
	/**
	 * 结束的投放日期
	 */
	private String endTime;
	/**
	 * 广告播放时长
	 */
	private String playTime;
	/**
	 * 广告投放日期
	 */
	private String pushTime;
	/**
	 * 广告状态
	 */
	private Integer status;
	/**
	 * 广告播放次数
	 */
	private Integer playCount;
	/**
	 * 用户观看次数
	 */
	private Integer userSawTimes;
	/**
	 * 广告名称
	 */
	private String name;
	/**
	 *合同编号 
	 */
	private String contractNumber;
	/**
	 * 广告是否暂停 1:正在使用，0：已经暂停
	 */
	private Integer enable;
	
	/**
	 * 权限等级
	 */
	private String roleLevel;
	/**
	 * 客户名称
	 */
	private String customerName;
	/**
	 * 广告类型 1：视频广告，2：音频广告
	 */
	private String classify;
	
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getPlayTime() {
		return playTime;
	}

	public void setPlayTime(String playTime) {
		this.playTime = playTime;
	}

	public String getPushTime() {
		return pushTime;
	}

	public void setPushTime(String pushTime) {
		this.pushTime = pushTime;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Integer getPlayCount() {
		return playCount;
	}

	public void setPlayCount(Integer playCount) {
		this.playCount = playCount;
	}

	public Integer getUserSawTimes() {
		return userSawTimes;
	}

	public void setUserSawTimes(Integer userSawTimes) {
		this.userSawTimes = userSawTimes;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getContractNumber() {
		return contractNumber;
	}

	public void setContractNumber(String contractNumber) {
		this.contractNumber = contractNumber;
	}

	public Integer getEnable() {
		return enable;
	}

	public void setEnable(Integer enable) {
		this.enable = enable;
	}

	public String getRoleLevel() {
		return roleLevel;
	}

	public void setRoleLevel(String roleLevel) {
		this.roleLevel = roleLevel;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getClassify() {
		return classify;
	}

	public void setClassify(String classify) {
		this.classify = classify;
	}

	@Override
	public String toString() {
		return "AdMaterialTotalPage [id=" + id + ", startTime=" + startTime + ", endTime=" + endTime + ", playTime="
				+ playTime + ", pushTime=" + pushTime + ", status=" + status + ", playCount=" + playCount
				+ ", userSawTimes=" + userSawTimes + ", name=" + name + ", contractNumber=" + contractNumber
				+ ", enable=" + enable + ", roleLevel=" + roleLevel + ", customerName=" + customerName + ", classify="
				+ classify + "]";
	}
	
	
}

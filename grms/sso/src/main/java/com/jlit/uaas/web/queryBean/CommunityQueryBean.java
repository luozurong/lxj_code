package com.jlit.uaas.web.queryBean;

public class CommunityQueryBean extends BaseQueryBean {
	
	private String adminTel;
	
	
	private String communityName;
	
	
	public String getAdminTel() {
		return adminTel;
	}

	public void setAdminTel(String adminTel) {
		this.adminTel = adminTel;
	}

	public String getCommunityName() {
		return communityName;
	}

	public void setCommunityName(String communityName) {
		this.communityName = communityName;
	}

	private String belongAccount;

	public String getBelongAccount() {
		return belongAccount;
	}

	public void setBelongAccount(String belongAccount) {
		this.belongAccount = belongAccount;
	}

	private String communityNum;
	
	public String getCommunityNum() {
		return communityNum;
	}

	public void setCommunityNum(String communityNum) {
		this.communityNum = communityNum;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	private String name;
	
	
}
